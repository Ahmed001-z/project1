    {
        $this->validate($request,[

            'name'=>'required',
            'address'=>'required',
            'phone'=>'required'

        ]);

        $blog=new companies();
        $blog->name=$request->name;
        $blog->Address=$request->address;
        $blog->Phone=$request->phone;


        $blog->save();
        return redirect()->back();
    }

