    {
        $this->validate($request,[

            'name'=>'required',
            'companies_id'=>'required',

        ]);

        $blog=new majors();
        $blog->name=$request->name;
        $blog->companies_id=$request->companies_id;



        $blog->save();
        return redirect()->back();
    }

